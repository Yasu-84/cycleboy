import { determineLineName } from '../src/lib/utils/lineUtils';

const tests = [
  { p: ['北海道', '青森', '岩手'], e: '北日本ライン' },
  { p: ['北海道', '青森', '東京'], e: '北日本ライン' }, // 北日本2, 関東1
  { p: ['北海道', '東京'], e: '混合ライン' }, // 北日本1, 関東1 -> 全て異なる -> 混合
  { p: ['東京', '神奈川'], e: '東日本ライン' }, // 関東1, 南関東1
  { p: ['福岡'], e: '単騎' }, // 九州1
  { p: ['東京', '埼玉', '神奈川'], e: '関東ライン' }, // 関東2, 南関東1 -> 関東ライン (多い方を表示)
  // 関東と南関東が同数の場合
  { p: ['東京', '埼玉', '神奈川', '千葉'], e: '東日本ライン' }, // 関東2, 南関東2 -> 関東と南関のみなので東日本ライン
];

for (const t of tests) {
  const r = determineLineName(t.p);
  console.log(`[${t.p.join(', ')}] => ${r} (expected: ${t.e})`);
}
